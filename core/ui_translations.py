DEFAULT_UI_LANG = "ru"
SUPPORTED_UI_LANGS = {"ru", "kk", "en"}


def get_ui_lang(request):
    lang = request.session.get("ui_lang") or request.COOKIES.get("ui_lang") or DEFAULT_UI_LANG
    if lang not in SUPPORTED_UI_LANGS:
        lang = DEFAULT_UI_LANG
    return lang